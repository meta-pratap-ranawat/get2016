/**
 * this application is action to be taken regarding menu item
 * */
public class Action{

	void menuItemAction(String item){

		switch (item){
		case "create":
			System.out.print("Action will be create");
			break;

		case "Open":
			System.out.print("Action will be open");
			break;

		case "Save":
			System.out.print("Action will be save");
			break;

		
		}

	}
}
